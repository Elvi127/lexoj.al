class Book {
    constructor(id, title, author, img, views, publisher, year, category, description){
        this.id = id;
        this.title = title;
        this.author = author;
        this.img = img;
        this.views = views;
        this.publisher = publisher;
        this.year = year;
        this.category = category;
        this.description = description;
    }
}

export default Book;